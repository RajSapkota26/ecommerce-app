package com.IntermTermProject.com.slider;


public class SliderItem {
    private String image;
    private String name;

    public SliderItem() {
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
